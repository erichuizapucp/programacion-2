class Traduccion {
    private String idioma;
    private String texto;
    
    public String getIdioma() {
        return idioma;
    }

    public String getTexto() {
        return texto;
    }
    
    public void setIdioma(String idioma) {
        this.idioma = idioma;
    }

    public void setTexto(String texto) {
        this.texto = texto;
    }
}